async function main() {
  console.log('Seeding merchant configs...');
  let created = 0;
  let updated = 0;

  for (const [costCentreTag, tagId, bankName, merchantCode, commissionRate, bankGlCode] of RAW_DATA) {
    const description = `${costCentreTag} | ${bankName}`;

    const existing = await prisma.merchantConfig.findFirst({
      where: { tagId, merchantCode },
    });

    if (existing) {
      await prisma.merchantConfig.update({
        where: { id: existing.id },
        data: { description, costCentreTag, bankName, commissionRate, bankGlCode, isActive: true },
      });
      updated++;
    } else {
      await prisma.merchantConfig.create({
        data: { description, costCentreTag, tagId, bankName, merchantCode, commissionRate, bankGlCode, isActive: true },
      });
      created++;
    }
  }

  console.log(`Done: ${created} created, ${updated} updated (${created + updated} total)`);
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
