export async function seedInstitutes(prisma) {
  console.log('ðŸŒ± Seeding institutes...');
  let created = 0;
  let skipped = 0;
  for (const name of institutes) {
    try {
      const existing = await prisma.institute.findFirst({ where: { name } });
      if (existing) { skipped++; continue; }
      await prisma.institute.create({ data: { name, status: 'active' } });
      created++;
    } catch (error) {
      console.error(`Error seeding institute "${name}":`, error.message);
    }
  }
  console.log(`âœ… Institutes seeding completed: ${created} created, ${skipped} skipped`);
  return { created, skipped };
}
